public class switchCase {
    public static void main(String[] args) {
        int num = 3;

        switch(num){
            case 1 : System.out.println("This is Computer lab");
                    break;
            case 2 : System.out.println("this is Physics lab");
                    break;
            case 3 : System.out.println("this is Programming lab");
                    break;
            default : System.out.println("this is calssroom");
        }
    }
}
